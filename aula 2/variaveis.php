<h2><center>Variáveis</center></h2>
<p>
    São estruturas (memória) utilizadas 
    para armazenar dados que serão processados pelo 
    programa 
</p>

<p>
    Para criar uma variável em PHP basta colocar 
    símbolo de $ na frente de um nome e atribuir 
    um valor com o símbolo de igual. Exemplo:
<pre>
    $variavel = 10; 
    $nome = "Maria";
    $nota = 5.5;
</pre>
</p>

